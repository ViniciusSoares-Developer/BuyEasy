            <h1 class="bg-black p-3 rounded text-white text-center">Kanban</h1>

            <section class="px-5">
                <h2>O que é?</h2>
                Kanban é um sistema visual de gestão de trabalho, que busca conduzir cada tarefa por um fluxo
                predefinido de trabalho.

                <p>Em geral, o conceito de Kanban pode ser definido pelos seguintes itens:</p>

                <ul>
                    <li>
                        O sistema visual: um processo, definido em um quadro com colunas de separação, que permite
                        dividir o
                        trabalho em segmentos ou pelo seu status, fixando cada item em um cartão e colocando em uma
                        coluna
                        apropriada para indicar onde ele está em todo o fluxo de trabalho.
                    </li>
                    <li>Os cartões: que descrevem o trabalho real que transita por este processo.</li>
                    <li>
                        A limitação do trabalho em andamento: que permite atribuir os limites de quantos itens podem
                        estar
                        em
                        andamento em cada segmento ou estado do fluxo de trabalho.
                    </li>
                    <li>
                        Ou seja, o Kanban é um fluxo de trabalho que busca indicar (e limitar) o trabalho em andamento —
                        ou
                        WIP,
                        Work In Progress.
                    </li>
                </ul>

                <p>
                    O Kanban pode ser considerado também como uma metodologia ágil exatamente por ter o objetivo de
                    evitar a
                    procrastinação e render mais no dia a dia.
                </p>

                <p>
                    Isso acontece porque todo o sistema é pautado de uma forma organizada para tornar o workflow mais
                    produtivo.
                </p>

                <p>Essa lógica de pensamento é muito vista na metodologia lean, onde se evitam desperdícios.</p>

                <p>
                    A junção desses conceitos pode ser chamada de “lean Kanban”, onde o fluxo de trabalho segue por
                    etapas
                    de forma enxuta.
                </p>

                <p>
                    Portanto, o método Kanban pode ser descrito como um processo para melhorar gradualmente tudo o que
                    você
                    faz.
                </p>
