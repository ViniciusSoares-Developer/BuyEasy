            <h1 class="bg-black p-3 rounded text-white text-center">Scrum</h1>
            <section class="px-5">

                <h2>O que é?</h2>

                <p>
                    Scrum é um conjunto de boas práticas empregado no gerenciamento de projetos complexos, em que não se
                    conhece
                    todas as etapas ou necessidades.
                </p>

                <p>
                    Focado nos membros da equipe, o Scrum torna os processos mais simples e claros, pois mantém
                    registros
                    visíveis sobre o andamento de todas as etapas.
                </p>

                <p>
                    Assim, os participantes sabem em que fase o projeto está, o que já foi concluído e o que falta ser
                    feito
                    para a sua entrega.
                </p>

                <p>
                    A metodologia também possibilita que produtos sejam apresentados em menor tempo, sem deixar de lado
                    a
                    qualidade.
                </p>
                <p>
                    Ela é aplicada a partir de ciclos rápidos, chamados sprints, nos quais há um tempo determinado para
                    que
                    as
                    atividades sejam concluídas – geralmente, entre duas e quatro semanas.
                </p>

                <p>Priorizando a criatividade e fluidez nos processos, o Scrum segue seis princípios:</p>

                <ul>
                    <li>Flexibilidade dos resultados</li>
                    <li>Flexibilidade dos prazos</li>
                    <li>Times pequenos</li>
                    <li>Revisões constantes</li>
                    <li>Colaboração</li>
                    <li>Orientação a objetos.</li>
                    <li>Por possibilitar a correção de erros de maneira orgânica durante os sprints, o Scrum é
                        considerado
                        uma</li>
                    <li>metodologia ágil.</li>
                </ul>

