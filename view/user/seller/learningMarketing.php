        <section class="px-5" style="text-align: justify;">
            <h2>O que é uma metodologia de vendas?</h2>
            <p>
                Uma metodologia de vendas é um conjunto de etapas que os vendedores devem seguir na hora de fazer um
                negócio. Ela divide a negociação em passos práticos, por exemplo, “fazer a pergunta X ao potencial
                cliente
                na etapa Y”.
            </p>
            <p>
                É importante não confundir metodologia e processo de vendas. Primeiro porque uma metodologia não se
                aplica a
                todo o ciclo de vendas: é voltada para algumas etapas específicas, como a qualificação, a abordagem e a
                conversão.
            </p>

            <p>
                Além disso, o processo comercial é exclusivo de cada empresa: deve ser desenvolvido com base no seu
                mercado
                de atuação, ofertas, posicionamento, entre outras características. Por outro lado, uma mesma metodologia
                pode ser implementada por diferentes negócios.
            </p>

